def format_percentage(value: float, include_sign: bool = True) -> str:
    """Format percentage with appropriate sign"""
    if include_sign:
        sign = "+" if value > 0 else ""
        return f"{sign}{value:.2f}%"
    return f"{value:.2f}%"


